// Default group standings — every group pre-seeded in FIFA-rank order (best
// rank → 1st place). The Road-to-the-Final screen starts here so the player
// only reorders the upsets instead of seating all 48 teams from scratch.
// Returns a fresh object (and fresh inner arrays) each call so callers can
// mutate freely without aliasing window.GROUPS.
window.rankedGroups = function () {
  const out = {};
  Object.keys(window.GROUPS).forEach((letter) => {
    out[letter] = window.GROUPS[letter]
      .slice()
      .sort((a, b) => a.rank - b.rank)
      .map((t) => t.code);
  });
  return out;
};

