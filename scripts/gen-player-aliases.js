#!/usr/bin/env node
// Regenerate festival/player-aliases.js + scripts/player-ids.lock.json after a
// squad-data refresh, so a re-IDed player never silently breaks saved picks.
//
//   node scripts/gen-player-aliases.js [baselineDataFile]
//
// Matches players by nation + folded name to detect re-IDs (old id → current id),
// MERGES with the existing alias map (collapsing chains so every old id resolves
// to the CURRENT player), and grows the id-lock append-only. Baseline defaults to
// the last committed data.js (git HEAD). After running, commit both files; the
// freeze test in verify-scoring.js then guards against future silent drops.

const { execSync } = require("child_process");
const vm = require("vm"), fs = require("fs"), path = require("path");
const ROOT = path.join(__dirname, "..");

function loadPlayers(src, fname) {
  const s = { window: {} };
  vm.createContext(s);
  vm.runInContext(src, s, { filename: fname, timeout: 5000 });
  return s.window.PLAYERS || [];
}
const fold = (s) => String(s || "").normalize("NFD").replace(/[̀-ͯ]/g, "").toLowerCase().replace(/[.'-]/g, " ").trim();

const baselineArg = process.argv[2];
const baselineSrc = baselineArg
  ? fs.readFileSync(baselineArg, "utf8")
  : execSync("git show HEAD:festival/data.js", { cwd: ROOT }).toString();
const oldP = loadPlayers(baselineSrc, "baseline");
const newP = loadPlayers(fs.readFileSync(path.join(ROOT, "festival/data.js"), "utf8"), "current");

const newById = new Map(newP.map((p) => [p.id, p]));
const newByKey = new Map(); newP.forEach((p) => newByKey.set(p.nat + "|" + fold(p.name), p));

let aliases = {};
try { aliases = { ...(require("../festival/player-aliases").PLAYER_ID_ALIASES || {}) }; } catch (e) {}
let added = 0;
oldP.forEach((o) => {
  if (newById.has(o.id)) return;
  const m = newByKey.get(o.nat + "|" + fold(o.name));
  if (m && !aliases[o.id]) { aliases[o.id] = m.id; added++; }
});
// Collapse chains (A→B, B→C ⇒ A→C) and drop aliases whose target no longer exists.
let changed = true;
while (changed) { changed = false; for (const k of Object.keys(aliases)) { const t = aliases[k]; if (aliases[t] && aliases[t] !== t) { aliases[k] = aliases[t]; changed = true; } } }
for (const k of Object.keys(aliases)) { if (!newById.has(aliases[k]) || aliases[k] === k) delete aliases[k]; }
const sorted = {}; Object.keys(aliases).sort().forEach((k) => { sorted[k] = aliases[k]; });

const body =
  "// STAR XI — player-ID aliases (AUTO-GENERATED by scripts/gen-player-aliases.js).\n" +
  "// Maps RETIRED player ids -> their current id, so entries saved before a squad\n" +
  "// refresh still resolve (and still score). NEVER hand-edit: regenerate, or add a\n" +
  "// new line when scripts/verify-scoring.js flags a dropped id. See task: freeze ids.\n" +
  "(function (root, factory) {\n" +
  "  var api = factory();\n" +
  "  if (typeof module !== \"undefined\" && module.exports) module.exports = api;\n" +
  "  if (typeof window !== \"undefined\") { window.PLAYER_ID_ALIASES = api.PLAYER_ID_ALIASES; window.resolvePid = api.resolvePid; }\n" +
  "})(this, function () {\n" +
  "  var PLAYER_ID_ALIASES = " + JSON.stringify(sorted, null, 2).replace(/\n/g, "\n  ") + ";\n" +
  "  function resolvePid(id) { return (id && PLAYER_ID_ALIASES[id]) || id; }\n" +
  "  return { PLAYER_ID_ALIASES: PLAYER_ID_ALIASES, resolvePid: resolvePid };\n" +
  "});\n";
fs.writeFileSync(path.join(ROOT, "festival/player-aliases.js"), body);

// Lock grows append-only: once an id could be picked, it must always resolve.
let lock = [];
try { lock = require("./player-ids.lock.json"); } catch (e) {}
const lockSet = new Set(lock); newP.forEach((p) => lockSet.add(p.id));
const lockArr = Array.from(lockSet).sort();
fs.writeFileSync(path.join(ROOT, "scripts/player-ids.lock.json"), JSON.stringify(lockArr, null, 0));

console.log(`aliases: ${Object.keys(sorted).length} (${added} new)  |  lock: ${lockArr.length} ids`);
console.log("Next: run `node scripts/verify-scoring.js` and commit both files.");
